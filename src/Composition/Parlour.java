package Composition;

public class Parlour {
    public int cushion;
    public int table;
    public int television;

    public int getCushion() {
        return cushion;
    }

    public void setCushion(int cushion) {
        this.cushion = cushion;
    }

    public int getTable() {
        return table;
    }

    public void setTable(int table) {
        this.table = table;
    }

    public int getTelevision() {
        return television;
    }

    public void setTelevision(int television) {
        this.television = television;
    }
}
