package Composition;

public class Kitchen {

    public int burner;
    public String food;
    public int cutlery;

    public int getBurner() {
        return burner;
    }

    public void setBurner(int burner) {
        this.burner = burner;
    }

    public String getFood() {
        return food;
    }

    public void setFood(String food) {
        this.food = food;
    }

    public int getCutlery() {
        return cutlery;
    }

    public void setCutlery(int cutlery) {
        this.cutlery = cutlery;
    }
}
